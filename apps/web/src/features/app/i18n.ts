export type MessageKey = keyof typeof messages.ru;
export type MembershipStatusValue = "inactive" | "active" | "expired";

const membershipStatusLabels: Record<Locale, Record<MembershipStatusValue, string>> = {
  ru: {
    inactive: "Доступ закрыт",
    active: "Доступ открыт",
    expired: "Доступ закрыт"
  },
  en: {
    inactive: "Access closed",
    active: "Access open",
    expired: "Access closed"
  }
};

export function formatMembershipStatus(status: MembershipStatusValue | string | null | undefined, language: Locale = locale.value) {
  const labels = membershipStatusLabels[language];
  if (status === "inactive" || status === "active" || status === "expired") {
    return labels[status];
  }

  return labels.inactive;
}

export function useI18n() {
  const t = (key: MessageKey) => messages[locale.value][key];
  const currentLocale = computed(() => locale.value);

  function setLocale(nextLocale: Locale) {
    locale.value = nextLocale;
    localStorage.setItem("club-locale", nextLocale);
  }

  return { currentLocale, setLocale, t };
}
